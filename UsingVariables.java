public class UsingVariables {
    
    public static void main(String[] args) {
        
        int roomNumber;
        double coolNumber;
        String funCourse;
        
        roomNumber = 113;
        coolNumber = 2.71828;
        funCourse = "Computer Science";
        
        System.out.println("This is room # " + (roomNumber) + ".");
        System.out.println("e is close to " + (coolNumber) + ".");
        System.out.println("I am learning a bit about " + (funCourse) + ".");
        
    }
}
